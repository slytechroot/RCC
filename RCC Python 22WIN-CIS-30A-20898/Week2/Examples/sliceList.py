#list slicing in Python

my_list = ['P','r','o','g','r','a','m']
print(my_list)

#output elements 3rd to 5th
print(my_list[2:5])

#output elements beginning to 4th index
print(my_list[:-5])

#output elements 6th to end
print(my_list[5:])

#output elements beginning to end
print(my_list[:])

#elements beginning to end
print(my_list[:])
print(my_list)

