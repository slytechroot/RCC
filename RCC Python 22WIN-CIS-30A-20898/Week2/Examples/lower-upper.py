#Python programs using lower() and upper()

#change to lower-case
strng1 = "I am a STUDENT"
print(strng1.lower())

#change to upper case
print(strng1.upper())

#using islower()
print(strng1.islower())

#change to upper-case
print(strng1.upper())

#using isupper()
strng2 = "PYTHON"
print(strng2.isupper())