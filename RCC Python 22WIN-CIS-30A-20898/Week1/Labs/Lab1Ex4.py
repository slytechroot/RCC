# Create a program that asks the user to enter name, age, height and weight
# using input() function. Then use display() function to display the information
# entered by user.

#prompt for name
user_name = input ('Enter your name: ')
print("Hello " + user_name + "!")

#prompt for age
user_age = input ('Enter your age: ')
print(user_age)

#prompt for height
user_height = input ('Enter your height: ')
print(user_height)

#prompt for height
user_weight = input ('Enter your weight: ')
print(user_weight)

#narrative with print() function
print("So your name is ", user_name, ", and you're", user_age, ",with a height of",
      user_height, ",and your weight is", user_weight)
