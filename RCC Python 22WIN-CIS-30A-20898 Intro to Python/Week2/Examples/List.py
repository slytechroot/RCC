#Python - using lists
# the equivalent to an array in C++ or Java.
# it can have mixed data and data type

#list indexing
my_list = ['P','y','t','h','o','n']

#Output: P
print(my_list[1])

#Output: t
print(my_list[2])

#Output: o
print(my_list[4])

#Nested list
n_list = ['Happy', [2,0,1,5]]

#nested indexing
#output: second element in the first sub-list
print(n_list[0][1])

#Output: fourth element in the second sub-list
print(n_list[1][3])




