"""
24.Given the tuple nums = (10, 20, 30, 40),
create a Python list that will have the values of the nums tuple plus 10 to each
element and then print the new list with the updated values.
Hint: Your output should look like:  [20, 30, 40, 50]

"""
nums = [10, 20, 30, 40]
print(nums)

for i in range(len(nums)):
    nums [i] += 10
print(nums)



